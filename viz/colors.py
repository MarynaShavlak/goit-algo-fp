"""Спільні утиліти для кольорів — використовуються кількома viz-модулями.

`lerp_color` — лінійна (покомпонентна) інтерполяція між двома кольорами. На ній
будуються градієнти: гілок дерева Піфагора (`pythagoras.branch_color`) і кроків
обходу дерева (`task_5.generate_hex_color`). Формат входу/виходу (шкала 0-255 чи
0-1, HEX-рядок чи кортеж) — на розсуд викликача.
"""

def lerp_color(c0, c1, t: float) -> tuple[float, ...]:
    """Лінійна інтерполяція між кольорами c0 і c1 за параметром t ∈ [0, 1].

    Працює покомпонентно для кортежів будь-якої довжини й шкали (0-255 або
    0-1); повертає кортеж float. t = 0 -> c0, t = 1 -> c1.
    """
    return tuple(a + (b - a) * t for a, b in zip(c0, c1, strict=True))
